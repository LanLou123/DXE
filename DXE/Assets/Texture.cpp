RenderTexture::RenderTexture(UINT _width, UINT _height, DXGI_FORMAT _format) : Texture(), width(_width), height(_height){

	textureDESC = {};
	textureDESC.Dimension = D3D12_RESOURCE_DIMENSION_TEXTURE2D;
	textureDESC.Alignment = 0; // may be 0, 4KB, 64KB, or 4MB. 0 will let runtime decide between 64KB and 4MB (4MB for multi-sampled textures)
	textureDESC.Width = width; // width of the texture
	textureDESC.Height = height; // height of the texture
	textureDESC.DepthOrArraySize = 1; // if 3d image, depth of 3d image. Otherwise an array of 1D or 2D textures (we only have one image, so we set 1)
	textureDESC.MipLevels = 1; // Number of mipmaps. We are not generating mipmaps for this texture, so we have only one level
	textureDESC.Format = _format; // This is the dxgi format of the image (format of the pixels)
	textureDESC.SampleDesc.Count = 1; // This is the number of samples per pixel, we just want 1 sample
	textureDESC.SampleDesc.Quality = 0; // The quality level of the samples. Higher is better quality, but worse performance
	textureDESC.Layout = D3D12_TEXTURE_LAYOUT_UNKNOWN; // The arrangement of the pixels. Setting to unknown lets the driver choose the most efficient one
	textureDESC.Flags = D3D12_RESOURCE_FLAG_ALLOW_RENDER_TARGET | D3D12_RESOURCE_FLAG_ALLOW_SIMULTANEOUS_ACCESS;

	depthStencilDESC = {};
	depthStencilDESC.Dimension = D3D12_RESOURCE_DIMENSION_TEXTURE2D;
	depthStencilDESC.Alignment = 0; // may be 0, 4KB, 64KB, or 4MB. 0 will let runtime decide between 64KB and 4MB (4MB for multi-sampled textures)
	depthStencilDESC.Width = width; // width of the texture
	depthStencilDESC.Height = height; // height of the texture
	depthStencilDESC.DepthOrArraySize = 1; // if 3d image, depth of 3d image. Otherwise an array of 1D or 2D textures (we only have one image, so we set 1)
	depthStencilDESC.MipLevels = 0; // Number of mipmaps. We are not generating mipmaps for this texture, so we have only one level
	depthStencilDESC.Format = DXGI_FORMAT_R24G8_TYPELESS; // This is the dxgi format of the image (format of the pixels)
	depthStencilDESC.SampleDesc.Count = 1; // This is the number of samples per pixel, we just want 1 sample
	depthStencilDESC.SampleDesc.Quality = 0; // The quality level of the samples. Higher is better quality, but worse performance
	depthStencilDESC.Layout = D3D12_TEXTURE_LAYOUT_UNKNOWN; // The arrangement of the pixels. Setting to unknown lets the driver choose the most efficient one
	depthStencilDESC.Flags = D3D12_RESOURCE_FLAG_ALLOW_DEPTH_STENCIL;
}

